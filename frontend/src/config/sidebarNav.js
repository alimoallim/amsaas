/** Flatten all routable items (for search / command palette) */
export function flattenSidebarNav(entries = sidebarNavigation) {
  const flat = []
  for (const entry of entries) {
    if (entry.type === 'link') {
      flat.push({ to: entry.to, label: entry.label, icon: entry.icon })
    } else if (entry.type === 'group') {
      for (const item of entry.items) {
        flat.push({ to: item.to, label: item.label, icon: item.icon, group: entry.label })
      }
    }
  }
  return flat
}
